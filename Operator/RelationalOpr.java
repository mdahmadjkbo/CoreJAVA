class RelationalOpr
{
    public static void main(String[]args)
    {
	int num1=45;
	int num2=76;
	System.out.println(num1>num2);
	System.out.println(num2<num1);
	System.out.println(3<3);
//------------------------------------------------------
	System.out.println(num1>=num2);
	System.out.println(num2>=num1);
	System.out.println(2>=2);
//------------------------------------------------------
	System.out.println(num1<=num2);
	System.out.println(num2>=num1);
	System.out.println(2>=2);
//------------------------------------------------------
	System.out.println(num1==num2);
	System.out.println(7==7);
//------------------------------------------------------
	System.out.println(num1!=num2);
	System.out.println(6!=6);
    }
}
