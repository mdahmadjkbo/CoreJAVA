class ModulusOp 
{
    public static void main(String[]args)
    {
	byte b=4;
	short s=2;
	float f=b%s;
	int i=9;
	long l=7;
	double d=i%l;
	System.out.println();
	System.out.println(f);//0.0(remainder)
	System.out.println();
	System.out.println(d);//2.0(remainder)
	System.out.println();
    }
}


//Modulus Operator(%)- The symbol % is used for performing Division and its output will be the Remainder.
