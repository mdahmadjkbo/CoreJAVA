class AssignNo
{
    public static void main(String[]args)
    {
	int num=5;
	num*=3;//num=num*3=15
	System.out.println(num);//15
	num/=2;//num=num/2=7
	System.out.println(num);//7
	num+=1;//num=num+1=8
	System.out.println(num);//8
	num%=2;//num=num%2=0//Global Operator(%)- Used to perform Division and its output is Remainder.
	System.out.println();//Null value(println statement with null value can be used for spacing between 2 lines in output)
	System.out.println(num);//num=0
    }
}
