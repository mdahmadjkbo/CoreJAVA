class IncDecOp
{
    public static void main(String[]args)
    {
	int i=20;
	System.out.println(++i);//21
	System.out.println(i);////21
	System.out.println(i++);//21
	System.out.println(i);///////22
	System.out.println(--i);//21
	System.out.println(i);////21
	System.out.println(i--);//21
	System.out.println(i);///////20
    }
}