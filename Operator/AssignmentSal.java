class AssignmentSal
{
    public static void main(String[]args)
    {
	int sal=15000;
	sal-=6000;//sal=sal-6000
	sal-=2000;//sal=sal-2000
	sal-=1000;//sal=sal-1000
	sal+=1000;//sal=sal+1000
	sal*=2;//sal=sal*2
	System.out.println(sal);//sal=14000
    }
}


