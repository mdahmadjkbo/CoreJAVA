class Subtraction
{
    public static void main(String[]args)
    {
	long l=20;
	float f=55.2f;
	float res1=f-l;
	float res2=-f+l;
	System.out.println();
	System.out.println(res1);//35.2
	System.out.println();
	System.out.println(res2);//-35.2
	System.out.println();
	System.out.println("Subtraction of"+ f+"and"+ l+" is" +res1);//Subtraction of 55.2 and 20 is 35.2
	System.out.println();
	System.out.println("Subtraction of"+ l +"and"+ f+" is" +res2);//Subtraction of 20 and 55.2 is -35.2
	System.out.println();
	System.out.println("Subtraction of l and f is" +res1);//Subtraction of l and f is 35.2
	System.out.println();
	System.out.println("Subtraction of 55.2 and 20 is" +res1);//Subtraction of 55.2 and 20 is 35.2
	System.out.println();
	System.out.println("Subtraction of 55.2 and 20 is" +"res1");//Subtraction of 55.2 and 20 is res1
	System.out.println();
     }
}