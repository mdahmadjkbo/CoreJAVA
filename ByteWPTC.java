class ByteWPTC
{
    public static void main(String[]args)
    {
	byte a=12;
	short b=a;
	int c=a;
	long d=a;
	float e=a;
	double f=a;
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
	System.out.println(f);
	System.out.println();
	System.out.println();
    }
}